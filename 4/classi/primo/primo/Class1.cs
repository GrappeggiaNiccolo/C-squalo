using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ClasseRettangolo
{
    internal class Rettangolone
    {
        //attributi
        public double Base;//i valori non assegnati vengono inizializzati a 0
        public double altezza;
        //Metodi
        //Metodo Costruttore
        public Rettangolone()
        {

        }
        public Rettangolone(double Base, double altezza)
        {
            this.Base = Base;
            this.altezza = altezza;
        }
        //Metodi Attributi
        public void SetBase(double Base)
        {
            this.Base = Base;
        }
        public void SetAltezza(double altezza)
        {
            this.altezza = altezza;
        }
        public double GetBase()
        {
            return this.Base;
        }
        public double GetAltezza()
        {
            return this.altezza;
        }
        public double GetArea()
        {
            return this.Base * this.altezza;
        }
        public double GetPerimetro()
        {
            return (this.Base * 2) + (this.altezza * 2);
        }
    }
}
